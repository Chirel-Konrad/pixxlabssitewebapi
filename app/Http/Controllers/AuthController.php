    }

    // D√©connexion utilisateur (r√©vocation du token actuel + suppression de la session)
    public function logout(Request $request)
    {
        $user = $request->user(); // R√©cup√®re l'utilisateur connect√© via le token

        if (!$user) {
            return response()->json([
                'status' => false,
                'message' => 'Aucun utilisateur connect√©'
            ], 401);
        }

        // Si 2FA activ√©, on repasse en "inactive"
        if ($user->is_2fa_enable) {
            $user->status = 'inactive';
            $user->save();
        }

        // üîë R√©voquer le token Passport
        $request->user()->token()->revoke();

        // üîë Supprimer la session Laravel associ√©e
        session()->flush(); // Vide compl√®tement la session
        session()->invalidate(); // Invalide l‚ÄôID actuel
        session()->regenerateToken(); // Reg√©n√®re le CSRF token (s√©curit√©)

        return response()->json([
            'status' => true,
            'message' => 'D√©connect√© avec succ√®s. Session et token r√©voqu√©s.'
        ], 200, [
            'Content-Type' => 'application/json; charset=UTF-8'
        ], JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT);
    }


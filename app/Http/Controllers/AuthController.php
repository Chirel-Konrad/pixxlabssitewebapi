    public function verifyEmail(Request $request, $id, $hash)
    {
        $user = User::findOrFail($id);

        // V√©rification du hash de l'email
        if (!hash_equals((string) $hash, sha1($user->getEmailForVerification()))) {
            return response()->json(['message' => 'Lien de v√©rification invalide.'], 400);
        }

        // V√©rifie si l'email est d√©j√† confirm√©
        if ($user->hasVerifiedEmail() && !$user->is_2fa_enable) {
            return response()->json(['message' => 'Email d√©j√† v√©rifi√©.'], 200);
        }

        // Marque l'email comme v√©rifi√©
        $user->markEmailAsVerified();
        event(new Verified($user));

        // Met √† jour le statut de l'utilisateur
        $user->update(['status' => 'active']);

        return response()->json([
            'message' => 'Email v√©rifi√© avec succ√®s. Statut activ√©.',
            'user' => $user->makeHidden(['password']),
        ],200, [
            'Content-Type' => 'application/json; charset=UTF-8'
        ], JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT);
    }


    // Connexion utilisateur classique
    public function login(Request $request)
    {
        $request->validate([
            'email' => 'required|email',
            'password' => 'required|string',
        ]);

        $user = User::where('email', $request->email)->first();
//dd($user);
        if (!$user) {
            return response()->json(['message' => 'Utilisateur non trouv√©'], 404);
        }

        if ($user->status == 'banned') {
            return response()->json(['message' => 'Compte banni'], 403);
        }

        if (!Auth::attempt($request->only('email', 'password'))) {
            return response()->json(['message' => 'Identifiants invalides'], 401);
        }

        // V√©rification 2FA
        if ($user->is_2fa_enable && $user->status == 'inactive') {
            $user->sendEmailVerificationNotification();
            Auth::logout();

            return response()->json([
                'message' => 'Connexion r√©ussie, mais v√©rification 2FA requise. Consultez votre email.',
                'two_factor_required' => true
            ]);
        }

        // Cr√©ation du token Passport
        $token = $user->createToken('PolariixToken')->accessToken;

        // üîë Cr√©ation / ouverture de la session Laravel
        session([
            'user_id' => $user->id,
            'notifications' => [], // tu pourras y pousser les notifications non lues
        ]);

        return response()->json([
            'status' => true,
            'message' => 'Connexion r√©ussie',
            'token' => $token,
            'session_id' => session()->getId(), // Utile c√¥t√© front si tu veux exploiter la session
            'user' => $user->makeHidden(['password']),
        ], 200, [
            'Content-Type' => 'application/json; charset=UTF-8'
        ], JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT);
    }


    //activer/d√©sactiver la 2FA
    public function enable2FA(Request $request)
    {
        $user = $request->user();
        $user->is_2fa_enable = true;
        $user->status = 'inactive'; // bloquer acc√®s jusqu'√† validation
        $user->save();

        // Envoi du mail de v√©rification 2FA (via email verification)
        $user->sendEmailVerificationNotification();

        return response()->json(['message' => '2FA activ√©e, un email de validation vous a √©t√© envoy√©.'],200, [
            'Content-Type' => 'application/json; charset=UTF-8'

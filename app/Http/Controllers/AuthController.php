    public function resetPassword(Request $request)
    {
        $request->validate([
            'token' => 'required|string',
            'password' => 'required|string|min:8|confirmed',
        ]);

        $user = User::where('password_reset_token', $request->token)->first();

        if (!$user) {
            return response()->json(['message' => 'Token invalide'], 400);
        }

        // Optionnel: v√©rifier expiration (ex: 60 minutes)
        if (Carbon::parse($user->password_reset_sent_at)->addMinutes(10)->isPast()) {
            return response()->json(['message' => 'Token expir√©'], 400);
        }

        // Mettre √† jour le mot de passe et r√©activer le compte
        $user->password = bcrypt($request->password);
        $user->status = 'active';
        $user->password_reset_token = null;
        $user->password_reset_sent_at = null;
        $user->save();

        return response()->json(['message' => 'Mot de passe r√©initialis√© avec succ√®s'],200, [
            'Content-Type' => 'application/json; charset=UTF-8'
        ], JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT);
    }

